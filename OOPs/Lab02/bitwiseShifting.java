public class bitwiseShifting
{
    public static void main(String[] args) {
        System.out.println((byte)(729>>2));
        System.out.println((byte)(729<<2));
        System.out.println((byte)(729>>>2));
        System.out.println((byte)(-729>>2));
        System.out.println((byte)(-729<<2));
        System.out.println((byte)(-729>>>2));
        System.out.println((729>>2));
        System.out.println((729<<2));
        System.out.println((729>>>2));
        System.out.println((-729>>2));
        System.out.println((-729<<2));
        System.out.println((-729>>>2));
    }
}